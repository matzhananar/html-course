function authorization() {
    var x = document.getElementById("auth_form");

    if(x.style.display === "none"){
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function basket() {
    var x = document.getElementById("basket");

    if(x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}